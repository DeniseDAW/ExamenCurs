    public Examen getExamenTR() throws Maex {
        double a = 0;
        try {
            a = Double.parseDouble(tfAprovat.getText());
        } catch (NumberFormatException ex) {
            throw new NumberFormatException("El valor de l'aprovat estÃ  entre 0 i 10");
        }
        int np = 0;
        try {
            np = Integer.parseInt(tfNumPreguntes.getText());
        } catch (NumberFormatException ex) {
            throw new NumberFormatException("El nombre de preguntes ha de ser major a 0");
        }
        int nf = 0;
        try {
            nf = Integer.parseInt(tfNumFalladesQResten.getText());
        } catch (NumberFormatException ex) {
            throw new NumberFormatException("El nombre de fallades que resten un encert ha de ser 1 com a mÃ­nim");
        }
        return new ExamenTestResta(nf, np,
                LocalDate.parse(tfData.getText(), Mut.DTF),
                tfAssignatura.getText(),
                a);
    }

